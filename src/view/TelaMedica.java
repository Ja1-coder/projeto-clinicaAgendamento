    private void marcarExameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_marcarExameActionPerformed
        // TODO add your handling code here:
    String descricao = DescricaoExame.getText();
    String dataExame = campoAnoExame.getText()+"-"+campoMesExame.getText()+"-"+campoDiaExame.getText();
    String horaExame = campoHoraExame.getText()+":"+campoMinutoExame.getText();
    
    int idConsulta;

    try {
        idConsulta = Integer.parseInt(jTextFieldIDConsultaExame.getText());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "ID da consulta inválido!", "Erro", JOptionPane.ERROR_MESSAGE);
        return;
    }

 
    Exame exame = new Exame(descricao, dataExame, horaExame, idConsulta);

    
    ExameDAO exameDAO = new ExameDAO();
    exameDAO.salvarExame(exame);

    
    DescricaoExame.setText("");
    campoDiaExame.setText("");
    campoMesExame.setText("");
    campoAnoExame.setText("");
    campoHoraExame.setText("");
    campoMinutoExame.setText("");
    //campoResultadoExame.setText("");
    jTextFieldIDConsultaExame.setText("");
    }//GEN-LAST:event_marcarExameActionPerformed

