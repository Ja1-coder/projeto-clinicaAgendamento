    private void cancelarExameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelarExameActionPerformed
        // TODO add your handling code here:
    try {
        String idExameStr = IdExameCancelar.getText().trim();

        if (idExameStr.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, insira o ID do exame para cancelar.", "Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int idExame = Integer.parseInt(idExameStr);
        
        ExameDAO exameDAO = new ExameDAO();

        exameDAO.cancelarExame(idExame);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "O ID do exame deve ser um número válido.", "Erro", JOptionPane.ERROR_MESSAGE);
    }
      
    }//GEN-LAST:event_cancelarExameActionPerformed

