    private void ButtonCadastrarMedicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonCadastrarMedicoActionPerformed
        // TODO add your handling code here:
        
    String crm = jTextFieldCRMCadastrar.getText();
    String nome = jTextFieldNomeMedicoCadastrar.getText();
    String especialidade = especialidadeMedico.getText();
    String telefone = TelefoneMedicoCadastrar.getText();
    String email = emailMedicoCadastrar.getText();

    // Validações básicas
    if (crm.isEmpty() || nome.isEmpty() || especialidade.isEmpty() || telefone.isEmpty() || email.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Por favor, preencha todos os campos!", "Atenção", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Cria um objeto Medico
    Medico medico = new Medico(crm, nome, especialidade, telefone, email);

    // Salva o médico no banco de dados
    MedicoDAO medicoDAO = new MedicoDAO();
    medicoDAO.salvarMedico(medico);

    // Limpa os campos após salvar
    jTextFieldCRMCadastrar.setText("");
    jTextFieldNomeMedicoCadastrar.setText("");
    especialidadeMedico.setText("");
    TelefoneMedicoCadastrar.setText("");
    emailMedicoCadastrar.setText("");
        
    }//GEN-LAST:event_ButtonCadastrarMedicoActionPerformed

