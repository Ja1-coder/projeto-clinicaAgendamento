    private void BotaoAgendarConsultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoAgendarConsultaActionPerformed
        // TODO add your handling code here:
    String dataConsulta = campoAnoConsulta.getText()+ "-" + campoMesConsulta.getText() + "-" + campoDiaConsulta.getText();
    String horaConsulta = campoHoraConsulta.getText() + ":" + campoMinutoConsulta.getText();
    String observacao = ObservacaoConsulta.getText();
    String cpf_paciente = CPFConsulta.getText();
    String crm_medico = ((Medico)jComboBoxEscolherMedico.getSelectedItem()).getCrm();

    
    if (cpf_paciente.isEmpty() || crm_medico.isEmpty() || dataConsulta.isEmpty() || horaConsulta.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Por favor, preencha todos os campos obrigatórios!", "Atenção", JOptionPane.WARNING_MESSAGE);
        return;
    }
    Consulta consulta = new Consulta( dataConsulta,  horaConsulta,  observacao,  cpf_paciente,  crm_medico);
    ConsultaDAO consultaDAO= new ConsultaDAO();


    
    consultaDAO.salvarConsulta(consulta);


        
    }//GEN-LAST:event_BotaoAgendarConsultaActionPerformed

