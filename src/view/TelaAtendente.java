    private void BotaoCadastrarPacienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoCadastrarPacienteActionPerformed
        // TODO add your handling code here:
    String nome = NomePaciente.getText();
    String cpf = CPFPaciente.getText();
    String sexo = SexoPaciente.getText();
    String telefone = TelefonePaciente.getText();
    String cep = CEPPaciente.getText();
    String email = EmailPaciente.getText();
    String dia = campoDia.getText();  
    String mes = campoMes.getText();  
    String ano = campoAno.getText();
    
    
    if (dia.isEmpty() || mes.isEmpty() || ano.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, preencha o dia, mês e ano da data de nascimento.", "Erro", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Concatena para formar a data no formato 'YYYY-MM-DD'
    String dataNascimento = ano + "-" + mes + "-" + dia;

    
    Paciente paciente = new Paciente(cpf, nome, telefone, dataNascimento, email, sexo, cep);
    PacienteDAO pacienteDAO = new PacienteDAO();

    // Chama o método para salvar o paciente no banco de dados
    pacienteDAO.salvarPaciente(paciente);
    
    // Exibe uma mensagem de sucesso ou erro
    JOptionPane.showMessageDialog(this, "Paciente cadastrado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
    
    // Limpa os campos após o cadastro (opcional)
    NomePaciente.setText("");
    CPFPaciente.setText("");
    campoDia.setText("");
    campoMes.setText("");
    campoAno.setText("");
    SexoPaciente.setText("");
    TelefonePaciente.setText("");
    CEPPaciente.setText("");
    EmailPaciente.setText("");
        
    }//GEN-LAST:event_BotaoCadastrarPacienteActionPerformed

