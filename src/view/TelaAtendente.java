    private void BotaoCancelarConsultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoCancelarConsultaActionPerformed
      
         String idConsultaStr = IDConsultaCancelar.getText();

    // Validar se o campo está preenchido
    if (idConsultaStr.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Por favor, insira o ID da consulta a ser cancelada!", "Atenção", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Converter o ID para inteiro
    int idConsulta;
    try {
        idConsulta = Integer.parseInt(idConsultaStr);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "O ID da consulta deve ser um número válido!", "Erro", JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    ConsultaDAO consultaDAO =  new ConsultaDAO();

   
    consultaDAO.cancelarConsulta(idConsulta);

    // Limpar o campo de entrada
    IDConsultaCancelar.setText("");

    }//GEN-LAST:event_BotaoCancelarConsultaActionPerformed

